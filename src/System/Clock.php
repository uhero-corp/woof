<?php

namespace Woof\System;

/**
 * 現在時刻を整数値で取得するためのインタフェースです。
 *
 * 時刻取得処理を抽象化することで、実装の差し替え (システム時刻 / テスト用時刻) を可能にします。
 * このインタフェースを介して時刻を取得することで、時刻依存ロジックのテスト容易性を高めることができます。
 */
interface Clock
{
    /**
     * 現在時刻を取得します。
     *
     * 返却値は通常 Unix Time (秒単位) を想定しています。
     *
     * @return int 現在時刻を表す整数値
     */
    public function getTime(): int;
}
